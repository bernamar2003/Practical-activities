package Ex06;

 public class EnhancedForLoopExample {
    public static void main(String[] args) {
        int[] numbers = {10, 20, 30, 40, 50};

        // This enhanced for loop iterates through the 'numbers' array.
        // In each iteration, the value of the current element is assigned to the 'number' variable.
        for (int number : numbers) {
            System.out.println(number);
        }
    }
    
}
